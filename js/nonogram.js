function hints(hintToGet) {
    let toCalculate = "";

    let hints = [];
    let occurrence = 0;
    let allHints = [];


    for (let i = 0; i < size; i++) {

        if (hintToGet == "columns") {
            toCalculate = document.querySelectorAll('#tile-grid > div[data-column="' + i + '"]');
        }
        if (hintToGet == "rows") {
            toCalculate = document.querySelectorAll('#tile-grid > div[data-row="' + i + '"]');
        }

        for (let j = 0; j < toCalculate.length; j++) {
            if (toCalculate[j].dataset.answer == 1) {
                occurrence += 1;
            } else {
                if (occurrence != 0) {
                    hints.push(occurrence);
                }
                occurrence = 0;
            }
            if (j == size - 1) {
                if (occurrence != 0) {
                    hints.push(occurrence);
                }
                allHints.push(hints);
                hints = [];
                occurrence = 0;
            }
        }
    }


    for (let r = 0; r < allHints.length; r++) {
        let div = document.createElement('div');
        div.style.minWidth = tileSize + 'px';
        div.style.minHeight = tileSize + 'px';
        if (hintToGet == "columns") {
            div.classList.add("hints-col");
        }
        if (hintToGet == "rows") {
            div.classList.add("hints-row");
        }


        for (let i = 0; i < allHints[r].length; i++) {
            div.innerHTML += '<div>' + allHints[r][i] + '</div>';
        }

        if (hintToGet == "columns") {
            document.querySelector("#col-hints").appendChild(div);
        }
        if (hintToGet == "rows") {
            document.querySelector("#row-hints").appendChild(div);
        }

    }


}


function updateGuess() {
    tilesGuess = [...document.querySelectorAll('#tile-grid > div[data-toggled]')].map(e => parseInt(e.attributes["data-toggled"].value));

    if (tilesAnswer.length == tilesGuess.length) {
        checkWin();
    }
}


function checkWin() {
    let is_same = (tilesAnswer.length == tilesGuess.length) && tilesAnswer.every(function (element, index) {
        return element === tilesGuess[index];
    });
    if (is_same) {
        alert("Completed");
    }
}



